<div class="textwall">
  <h2>Il progetto RAM</h2>

  <h3>La nascita dell'idea</h3>
  <span>
    Il progetto nasce come “laboratorio di ricerca” che si occupa di monitorare 
    le capacità fisiche, motorie e funzionali degli studenti. <br>
    Questo progetto di ricerca vorrebbe valutare attraverso un percorso scientifico lo stato di benessere motorio degli studenti del Fauser dai 14 ai 18 anni; 
    è indubbiamente un impegno di grande portata.<br>
    Molto spesso infatti si denuncia che i giovani in generale fanno poca attività motoria o sportiva, che molti di essi sono in soprappeso o addirittura obesi. <br>
    L’idea di attivare un Database di ricerca per monitorare lo stato di benessere motorio degli studenti è stata dettata soprattutto dal fatto che molto spesso
    i media, le istituzioni, le associazioni di categoria, gli istituti di ricerca denunciano l’insufficienza di movimento dei giovani e il loro carente stato motorio. 
    Questa ricerca, attraverso una sperimentazione che partirà nell’a.s. 2017-2018 proverà a dare delle risposte concrete. 
  </span>
  
  <h3>L'esigenza di un database</h3>
  <span>
    L’obiettivo del RAM è quello di fare il punto della situazione sulle abilità motorie degli studenti, per poter poi giustificare 
    eventuali interventi didattici per potenziare l’attività motoria e l’educazione fisica all’interno della scuola. <br>
	L’insegnante dopo aver somministrato i test, potrà quindi valutare immediatamente e confrontare in tempo reale i propri studenti. <br>
	Il fatto di aver scelto dei protocolli già collaudati, e quindi già dotati di dati precedentemente rilevati e validati,
    consentirà all’insegnante di collocare le prestazioni dei propri studenti all’interno di tabelle di confronto già predisposte. 
  </span>
  
  <h3>Cos'&egrave; un test motorio?</h3>
  <span>
    Attraverso i test motori possiamo misurare il livello di diverse capacità motorie quali la forza, la rapidità, la resistenza.<br>
	Essendo di fatto impossibile misurare tali capacità, definite come concetto astratto, si ricorre all’utilizzo di INDICATORI, cioè 
    elementi concretamente misurabile che ne “indicano” un altro che la teoria e metodologia dell’allenamento ritiene inequivocabilmente correlato. <br>
	Per valutare le capacità ed attitudini di un soggetto di norma non si ricorre ad un test unico ed omnicomprensivo (del resto non approntabile), 
    ma ad una batteria di test che misurando le diverse capacità motorie possa fornire una valutazione allo stesso tempo sintetica ed analitica 
    (in quanto ogni test specifico concorre alla valutazione complessiva).<br>
	L'affidabilità di un test motorio dipende da diversi fattori, quali la strutturazione del test e le diverse modalità di somministrazione e di misurazione. 
	<br>I test motori debbono possedere le seguenti caratteristiche : 
    <ul>
      <li>STANDARDIZZAZIONE</li> 
      <li>VALIDIT&Agrave;</li>
      <li>OGGETTIVIT&Agrave;</li>
      <li>ATTENDIBILIT&Agrave;</li> 
      <li>SELETTIVIT&Agrave;</li>
    </ul>
  </span>
  
  <h3>La scelta dei test</h3>
  <span>
    La scelta ha tenuto conto di alcune linee guida: 
    <ol>
      <li>Che i protocolli fossero stati validati a livello internazionale;</li> 
      <li>Che fossero praticabili nella scuola;</li> 
      <li>Che non necessitassero di strumenti troppo onerosi e introvabili a scuola</li>
	</ol>
  </span>

  <h3>Per chi?</h3>
  <span>
    L’intento che ci proponiamo attraverso questa ricerca non è solo quello di dare delle risposte concrete al primo quesito 
    che ci siamo proposti con questo progetto, cioè monitorare lo stato di benessere motorio degli studenti ma anche 
    quello di offrire un servizio a tutti gli insegnanti che parteciperanno alla ricerca, perché conoscere i “prerequisiti” 
    dei propri alunni con i quali e per i quali si lavora è l’inizio della costruzione di un itinerario.<br>
	Ma non dimentichiamo la cosa più importante: per lo studente, perché conoscere questi punti di riferimento significa,
    innanzitutto, rendersi conto dei propri attuali limiti e, di conseguenza, individuare la base da cui partire nella 
    ricerca di qualsiasi miglioramento.
  </span>

  <h3>A cosa serve RAM?</h3>
  <span>
    Obiettivi del progetto:
    <ul>
      <li>
        Far prendere coscienza delle proprie caratteristiche fisiche e psicomotorie, utili nelle attività sportive ed in generale
        nel mantenimento delle “salute dinamica”; <br>
        scoprire le proprie capacità “funzionali”, che debbono consentire una fisiologica capacità osteo-articolare ed un equilibrato tono muscolare.
      </li>
	  <li>
        Raccolta ed analisi dei dati rilevati, con lo scopo di seguire l’evoluzione fisica e 
        motoria degli alunni e di stilare delle fasce di “competenza motoria” della nostra popolazione scolastica, 
        così da ottenere dei punti di riferimento per le attività future.
      </li>
	</ul>
    Nel Progetto RAM verranno eseguiti una serie di test motori per indagare le qualità motorie(capacità condizionali e coordinative)
	per tutti gli alunni dell’Istituto.<br>
	Sarà composto da una batteria di 9/12 test per valutare le capacità motorie quali ad esempio 
    la mobilità articolare, la tonicità muscolare, la forza esplosiva, la capacità aerobica, la velocità, la coordinazione dinamica. 
  </span>

  <h3>A chi?</h3>
  <span>
    Il progetto riguarda tutti gli studenti del ITIS Fauser, con la speranza di poterlo estendere agli altri Istituti della provincia,
    al fine di realizzare un ampio “data base” sulle capacità dei nostri giovani da cui attingere per attuare percorsi specifici di 
    competenze motorie.<br>
	A tal proposito, tutti i dati rilevati, ogni anno, verranno trasmessi all’Ufficio Scolastico Provinciale di Novara – Ufficio Educazione Fisica. 
  </span>

  <h3>Come si svolgerà?</h3>
  <span>
    Articolazione del progetto 

	Il primo passo sarà la spiegazione e l’esecuzione dei test, quindi il rilevamento dei risultati ottenuti. 
	Il rilevamento verrà eseguito dagli insegnanti di educazione fisica ed effettuato sempre nelle stesse condizioni 
    logistiche e con le medesime attrezzature (con cui rendere il più possibile obiettivi i test).<br>
	I dati raccolti verranno distinti per sesso ed età, trascritti su database RAM.<br>
	I risultati dei test motori verranno differenziati per sesso ed età, sono quindi estrapolati i dati per la 
    composizione delle fasce di livello e per la realizzazione dei parametri di riferimento in “centili”.<br>
	Accedendo al Database RAM verranno preparati i risultati dei propri test ed i parametri di riferimento per la valutazione 
    (saranno riportati anche i valori delle eventuali prove degli anni precedenti, per un immediato riscontro).<br>
	A tutti gli alunni verranno consegnati i risultati ottenuti; messi a conoscenza delle eventuali caratteristiche da ottimizzare, 
    verranno proposte esercitazioni specifiche.
  </span>

  <h3>Strutturazione</h3>
  <span>
    Per verificare lo stato di salute motoria degli studenti la ricerca si articolerà nei seguenti momenti di lavoro: 
    <ul>
      <li>Rilevazione dei Test motori:
        <ol>
          <li>Flessibilità della cerniera dorso lombare</li>
          <li>Flessibilità dei Mm ischio crurali e flessori della coscia</li>
          <li>Flessibilità scapolo omerale</li>
          <li>Flessibilità coxo femorale</li>
          <li>Forza esplosiva arti inferiori</li>
          <li>Forza arti superiori</li>
          <li>Forza addominali</li>
          <li>Resistenza cardio-respiratoria</li>
        </ol>
      </li>
      <li>Raccolta dei dati</li>
      <li>Organizzazione dei dati in tabelle riassuntive</li>
      <li>Comparazione grafica e discorsiva dei campioni</li>
      <li>Discussione e critica dei risultati</li>
      <li>Considerazioni e conclusioni</li>
      <li>Divulgazione dei risultati</li>
    </ul>
  </span>
  
  <h3>Conclusioni</h3>
  <span>
    Il Database RAM compie elaborazioni sui test inseriti in una “Banca dei Test Motori”.<br>
	I test inseriti utilizzano indicatori di Capacità Condizionali e/o Coordinative ed Abilità che hanno valenza 
    trasversale rispetto a diversi ambiti di discipline sportive.
	Tali test verranno forniti a tutti gli utenti in ambito scolastico.
  </span>
  
